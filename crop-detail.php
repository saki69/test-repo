                Rice is the seed of the monocot plants Oryza sativa (Asian rice) or Oryza glaberrima (African rice). As a cereal grain, it is the most widely consumed staple food for a large part of the world's human population, especially in Asia. It is the grain with the second-highest worldwide production, after maize (corn), according to data for 2010.[1] Since a large portion of maize crops are grown for purposes other than human consumption, rice is the most important grain with regard to human nutrition and caloric intake, providing more than one fifth of the calories consumed worldwide by humans. Genetic evidence has shown that rice originates from a single domestication 8,200–13,500 years ago,[3] in the Pearl River valley region of China.[4] Previously, archaeological evidence had suggested that rice was domesticated in the Yangtze River valley region in China.[3] From East Asia, rice spread to Southeast and South Asia.[4] Rice was introduced to Europe through Western Asia, and to the Americas through European colonization. There are many varieties of rice and culinary preferences tend to vary regionally. In some areas such as the Far East or Spain, there is a preference for softer and stickier varieties. Rice is normally grown as an annual plant, although in tropical areas it can survive as a perennial and can produce a ratoon crop for up to 30 years.[5][dead link] The rice plant can grow to 1–1.8 m (3.3–5.9 ft) tall, occasionally more depending on the variety and soil fertility. It has long, slender leaves 50–100 cm (20–39 in) long and 2–2.5 cm (0.79–0.98 in) broad. The small wind-pollinated flowers are produced in a branched arching to pendulous inflorescence 30–50 cm (12–20 in) long. The edible seed is a grain (caryopsis) 5–12 mm (0.20–0.47 in) long and 2–3 mm (0.079–0.12 in) thick.
